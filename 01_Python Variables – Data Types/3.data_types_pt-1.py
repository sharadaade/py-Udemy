"""
--Data Type--         --Class--                 --Value--
integers               -> int                     -> 45
floating point numbers -> float                   -> 4.5
booleans               -> bool                    -> True/False
strings                -> str                     -> "Muslim Helalee"
list                   -> list                    -> [1, 2, 3]
dictionary             -> dict                    -> {"user_name": "awesome50", "user_id": 56}
tuple                  -> tuple                   -> (10, 20, 30)
set                    -> set                     -> {"cat", 99}
"""

# *-*-*-*-*--------------- Numbers **************************

# Integer
age = 38
print(age)
print(type(age))

# Floats
grade = 8.9
print(grade)
print(type(grade))

# *-*-*-*-*--------------- Booleans **************************
alarm = True
offline = False
print(alarm, offline)
print(type(alarm))
print(type(offline))

# *-*-*-*-*--------------- Strings **************************

movie_name = "The good, bad and ugly"
print(movie_name)
print(type(movie_name))
