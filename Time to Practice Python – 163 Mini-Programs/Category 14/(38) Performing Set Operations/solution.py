
set_a = {0, 1, 2, 3, 4, 5}
set_b = {11, 12, 13, 14, 15, 16}

# set union
print("Union of Set B & A is", set_b | set_a)
print()

# set intersection
print("intersection of Set B & A is", set_b & set_a)
print()


# set Difference
print("Difference of Set B & A is", set_b - set_a)
print()


# set Symmetric Difference
print("Symmetric Difference of Set B & A is", set_b ^ set_a)
print()
